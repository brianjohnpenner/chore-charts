import fs from "node:fs";
import path from "node:path";

const root = process.cwd();
const packageRoot = path.join(root, "node_modules", "@tabler", "icons");
const outlineDir = path.join(packageRoot, "icons", "outline");
const categoryRoot = path.join(packageRoot, "categories", "outline");
const outputPath = path.join(root, "assets", "tabler-icons.js");

function titleFromName(name) {
  return name
    .split("-")
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(" ");
}

function cleanSvg(svg) {
  return svg
    .replace(/\s+xmlns="[^"]+"/, "")
    .replace(/\s+width="[^"]+"/, "")
    .replace(/\s+height="[^"]+"/, "")
    .replace(/\s+class="[^"]+"/, "")
    .replace(/<path stroke="none" d="M0 0h24v24H0z" fill="none"\s*\/>\s*/g, "")
    .replace(/\s+/g, " ")
    .replace(/>\s+</g, "><")
    .trim();
}

function readCategoryMap() {
  const categoryByName = new Map();
  if (!fs.existsSync(categoryRoot)) return categoryByName;

  for (const category of fs.readdirSync(categoryRoot)) {
    const categoryDir = path.join(categoryRoot, category);
    if (!fs.statSync(categoryDir).isDirectory()) continue;

    for (const file of fs.readdirSync(categoryDir)) {
      if (!file.endsWith(".svg")) continue;
      categoryByName.set(path.basename(file, ".svg"), category);
    }
  }

  return categoryByName;
}

const categoryByName = readCategoryMap();
const iconMetadata = JSON.parse(fs.readFileSync(path.join(packageRoot, "icons.json"), "utf8"));
const files = fs.readdirSync(outlineDir)
  .filter((file) => file.endsWith(".svg"))
  .sort((a, b) => a.localeCompare(b));

const icons = Object.fromEntries(files.map((file) => {
  const name = path.basename(file, ".svg");
  return [name, cleanSvg(fs.readFileSync(path.join(outlineDir, file), "utf8"))];
}));

const options = files.map((file) => {
  const name = path.basename(file, ".svg");
  const metadata = iconMetadata[name] || {};
  const label = titleFromName(name);
  const category = metadata.category || categoryByName.get(name) || "";
  const tags = Array.isArray(metadata.tags) ? metadata.tags.map(String) : [];
  const search = `${label} ${name.replace(/-/g, " ")} ${category} ${tags.join(" ")}`.toLowerCase();

  return {
    label,
    icon: name,
    category,
    tags,
    search
  };
});

const version = JSON.parse(fs.readFileSync(path.join(packageRoot, "package.json"), "utf8")).version;
const source = `// Generated by scripts/generate-tabler-icons.mjs from @tabler/icons ${version}.
(function () {
  const icons = ${JSON.stringify(icons)};
  const options = ${JSON.stringify(options)};

  window.TablerIconRegistry = {
    version: ${JSON.stringify(version)},
    icons,
    options
  };
})();
`;

fs.writeFileSync(outputPath, source);
console.log(`Generated ${path.relative(root, outputPath)} with ${options.length} Tabler icons.`);
